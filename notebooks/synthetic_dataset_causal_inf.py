np.random.seed(42)

def generate_features():

    # Population density (right-skewed)
    population_density = np.random.lognormal(
        mean=7.4,
        sigma=0.8
    )
    population_density = np.clip(
        population_density,
        100,
        10000
    )

    # Average age
    age_mean = np.clip(
        np.random.normal(
            loc=42,
            scale=10
        ),
        20,
        75
    )

    # Vaccination increases slightly with age
    vaccination_rate = np.clip(
        np.random.beta(5,3)
        + 0.002*(age_mean-42),
        0,
        0.95
    )

    # Mask compliance
    mask = np.clip(
        np.random.beta(6,2),
        0.2,
        0.98
    )

    # Mobility
    mobility = np.clip(
        np.random.normal(
            1.0,
            0.18
        ),
        0.4,
        1.6
    )

    # Hospital capacity increases with density
    hospital_capacity = np.clip(
        150
        + 0.08*population_density
        + np.random.normal(0,120),
        100,
        1200
    )

    return {

        "population_density": population_density,
        "vaccination_rate": vaccination_rate,
        "mobility": mobility,
        "mask": mask,
        "hospital_capacity": hospital_capacity,
        "age_mean": age_mean

    }

def compute_contact_intensity(f):

    # Scale population density
    density_scaled = np.log1p(f["population_density"]) / np.log1p(10000)

    # Older populations tend to have slightly fewer contacts
    age_factor = (f["age_mean"] - 40) / 30

    # Better healthcare often reflects better awareness and public behaviour
    hospital_factor = (
        f["hospital_capacity"] - 150
    ) / 1050

    # Contact score
    x = (
        -0.8
        + 1.2 * density_scaled
        + 0.9 * f["mobility"]
        - 0.8 * f["mask"]
        - 0.6 * f["vaccination_rate"]
        - 0.3 * age_factor
        - 0.2 * hospital_factor
        + np.random.normal(0, 0.12)
    )

    # Logistic transformation
    contact = 0.2 + 1.0 / (1 + np.exp(-x))

    return contact

def compute_beta(f, contact):

    # Normalize population density
    density_factor = np.log1p(f["population_density"]) / np.log1p(10000)

    # Base transmission score
    x = (
        -1.0
        + 2.2 * contact
        + 0.5 * density_factor
        - 0.8 * f["vaccination_rate"]
        - 0.5 * f["mask"]
        + np.random.normal(0, 0.02)
    )

    # Logistic transformation
    beta = 0.18 + 0.70 / (1 + np.exp(-x))

    return np.clip(beta, 0.15, 0.90)

from scipy.special import expit

def assign_treatment(f, beta, contact):

    # Normalize hospital capacity
    hospital_factor = (
        f["hospital_capacity"] - 100
    ) / 1100

    # Normalize density
    density_factor = np.log1p(
        f["population_density"]
    ) / np.log1p(10000)

    # Government "risk score"
    risk_score = (

        -2.4

        # High transmission
        + 2.4 * beta

        # High contact behaviour
        + 1.2 * contact

        # Dense regions
        + 0.8 * density_factor

        # Better hospitals delay lockdown slightly
        - 0.5 * hospital_factor

        # High vaccination reduces need
        - 1.3 * f["vaccination_rate"]

        # Strong masking reduces need
        - 0.7 * f["mask"]

        # Random political / administrative variation
        + np.random.normal(0,0.25)

    )

    propensity = expit(risk_score)

    treatment = np.random.binomial(
        1,
        propensity
    )

    return treatment, propensity

def initial_state(f):

    # Vaccination reduces the susceptible population
    vaccination = f["vaccination_rate"]

    # Initial infectious proportion
    I0 = np.random.uniform(0.002, 0.008)

    # Initial exposed proportion
    E0 = np.random.uniform(0.003, 0.010)

    # Initial recovered population
    R0 = np.clip(
        vaccination * np.random.uniform(0.75, 0.95),
        0,
        0.90
    )

    # Remaining population is susceptible
    S0 = 1 - (E0 + I0 + R0)

    # Numerical safety
    S0 = max(S0, 0)

    return [S0, E0, I0, R0]

def beta_time(beta_base, t, lockdown, compliance):

    risk_multiplier = np.clip(beta_base / 0.60, 0.8, 1.4)


    effective_reduction = (
        lockdown
        * compliance
        * risk_multiplier
)

    beta = beta_base * (1 - 0.60 * effective_reduction)

    beta *= np.random.lognormal(
        mean=0,
        sigma=0.025
    )

    return np.clip(beta, 0.05, 1.20)

def generate_compliance(f, treatment):

    """
    Behavioural compliance with public health measures.
    """

    # Baseline voluntary compliance
    base = (
        0.25
        + 0.30 * f["mask"]
        + 0.15 * f["vaccination_rate"]
        - 0.10 * f["mobility"]
        + np.random.normal(0, 0.08)
    )

    if treatment == 1:

        # Lockdown increases compliance,
        # but people still vary.
        compliance = (
            base
            + 0.20
            + np.random.normal(0, 0.05)
        )

    else:

        compliance = base

    return np.clip(compliance, 0.15, 0.95)

