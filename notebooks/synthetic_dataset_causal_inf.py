def seir_model(
    t,
    y,
    beta_base,
    sigma,
    gamma,
    lockdown,
    compliance
):

    S, E, I, R = y

    # Time-varying transmission
    beta = beta_time(
        beta_base,
        t,
        lockdown,
        compliance
    )

    # Force of infection
    lambda_t = beta * I

    # SEIR equations
    dS = -lambda_t * S
    dE = lambda_t * S - sigma * E
    dI = sigma * E - gamma * I
    dR = gamma * I

    return [dS, dE, dI, dR]

def simulate_epidemic():

    # ----------------------------
    # Generate population features
    # ----------------------------
    f = generate_features()

    gamma = np.random.uniform(0.05, 0.40)
    sigma = np.random.uniform(0.10, 0.30)

    # ----------------------------
    # Contact behaviour
    # ----------------------------
    contact = compute_contact_intensity(f)

    # ----------------------------
    # Base transmission
    # ----------------------------
    beta = compute_beta(f, contact)

    # ----------------------------
    # Government intervention
    # ----------------------------
    treatment, propensity = assign_treatment(
        f,
        beta,
        contact
    )

    # ----------------------------
    # Lockdown strength
    # ----------------------------
    if treatment == 1:
        lockdown = np.random.uniform(0.30, 0.80)
    else:
        lockdown = 0.0

    # ----------------------------
    # Behavioural compliance
    # ----------------------------
    compliance = generate_compliance(
        f,
        treatment
    )

    # ----------------------------
    # Initial epidemic state
    # ----------------------------
    y0 = initial_state(f)

    t_eval = np.arange(0, 201)

    # ----------------------------
    # Counterfactual: No treatment
    # ----------------------------
    sol0 = solve_ivp(
        seir_model,
        [0, 200],
        y0,
        args=(
            beta,
            sigma,
            gamma,
            0.0,
            compliance
            ),
        t_eval=t_eval
    )

    # ----------------------------
    # Counterfactual: Treatment
    # ----------------------------
    sol1 = solve_ivp(
        seir_model,
        [0, 200],
        y0,
        args=(
            beta,
            sigma,
            gamma,
            lockdown,
            compliance
            ),
        t_eval=t_eval
    )

    I0 = sol0.y[2]
    I1 = sol1.y[2]

    Y0_peak = I0.max()
    Y1_peak = I1.max()

    ITE = Y1_peak - Y0_peak


    # ----------------------------
    # Factual outcome
    # ----------------------------
    if treatment == 1:
        observed_peak = Y1_peak
        peak_day = I1.argmax()
        area = np.trapz(I1)
        mean_inf = I1.mean()
        max_exp = sol1.y[1].max()
        final_rec = sol1.y[3][-1]

    else:
        observed_peak = Y0_peak
        peak_day = I0.argmax()
        area = np.trapz(I0)
        mean_inf = I0.mean()
        max_exp = sol0.y[1].max()
        final_rec = sol0.y[3][-1]

    return {

        "beta": beta,
        "contact_intensity": contact,

        "gamma": gamma,
        "sigma": sigma,

        "population_density": f["population_density"],
        "vaccination_rate": f["vaccination_rate"],
        "mobility": f["mobility"],
        "mask": f["mask"],
        "hospital_capacity": f["hospital_capacity"],
        "age_mean": f["age_mean"],

        "lockdown_strength": lockdown,
        "compliance": compliance,

        "R0": beta / gamma,

        "Y0_peak": Y0_peak,
        "Y1_peak": Y1_peak,
        "ITE_peak": ITE,

        "peak_infection": observed_peak,
        "peak_day": peak_day,

        "area_under_curve": area,
        "mean_infected": mean_inf,
        "max_exposed": max_exp,
        "final_recovered": final_rec,

        "propensity_score": propensity,
        "treatment": treatment

    }

