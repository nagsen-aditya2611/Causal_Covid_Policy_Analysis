def standardized_mean_difference(treated, control, column):

    mean_t = treated[column].mean()
    mean_c = control[column].mean()

    var_t = treated[column].var()
    var_c = control[column].var()

    pooled_sd = np.sqrt((var_t + var_c) / 2)

    return (mean_t - mean_c) / pooled_sd

before = []
